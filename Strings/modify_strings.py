# convert into uppercase
a = "Sumitra Samal"
print(a.upper())

# convert into lowercase
b = "Sumitra Samal"
print(b.lower())

# Remove white space using strip() funciton
c = "     Sumitra Shankar Samal  "
print(c.strip())

# replace string usingn replace() func
d = "Sumitra Samal"
print(d.replace("S", "K"))

# split string using split() func
e = "Sumitra,Samal"
print(e.split(","))