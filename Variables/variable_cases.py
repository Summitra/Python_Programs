#Camel Case
# Each word, except first, starts with capital letter
myVariableName = "John"
print(myVariableName)

#Pascal Case
#Each word starts with a capital letter
MyVariableName = "Sumitra"
print(MyVariableName)

#Snake Case
#Each word is separated by a underscore char
my_variable_name = "Samal"
print(my_variable_name)